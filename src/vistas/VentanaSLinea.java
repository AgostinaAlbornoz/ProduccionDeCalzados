    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        DefaultTableModel datos = (DefaultTableModel) jTable1.getModel();
        int nfila = jTable1.getSelectedRow();;

        if(nfila>=0){
            //Los numeros son el nro de columna al que pertenece
            
            String horaInicio = datos.getValueAt(nfila, 0).toString();
            String horaFin = datos.getValueAt(nfila, 1).toString();
            String modelo = datos.getValueAt(nfila, 2).toString();
            String color = datos.getValueAt(nfila, 3).toString();
            String objetivoHora = datos.getValueAt(nfila, 4).toString();
        
        
            //Mostramos


            jTextFieldHoraInicioSL.setText(horaInicio);
            jTextFieldHoraFinSL.setText(horaFin);
            jTextFieldObjetivoProdSL.setText(objetivoHora);
            jComboBoxModelo.setSelectedItem(modelo);
            jComboBoxColor.setSelectedItem(color);
            }
    }//GEN-LAST:event_jTable1MouseClicked

