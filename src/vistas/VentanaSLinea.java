    public void llenarJTable(){

        DefaultTableModel datos = (DefaultTableModel) jTable1.getModel();
        // recorrer las filas que tenemos, que vamos cargando y los pasamos al modelo
        
        datos.setNumRows(0); // hace que al cargar una nueva orden, no se dupliquen los datos
        jLinea.setText(""+nroOP());
        for (DatosSupLinea calzado : calzados) {
            eliminar();
            Object[] fila = {calzado.getHoraInicio(), calzado.getHoraFinal(), "DEPOR - " +calzado.getModelo()+" - " +calzado.getColor(),calzado.getHoraProduccion(), calzado.getNroLinea()};
            datos.addRow(fila);
        }
    }
    
